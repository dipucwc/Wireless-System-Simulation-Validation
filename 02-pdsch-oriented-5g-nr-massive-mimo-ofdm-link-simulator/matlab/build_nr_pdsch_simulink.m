%% Area labels and title, matching the testbench style:
%%

labels = { ...
    sprintf('5G NR PDSCH %dx%d MIMO-OFDM Link-Level Testbench', nTx, nRx), [520 20], 18; ...   % Dimension-derived
                                                   % title, so the compact and massive benches identify themselves.
    'TRANSMITTER',         [160 110], 12; ...
    'WIRELESS CHANNEL',    [620 110], 12; ...
    'RECEIVER',            [1150 110], 12; ...
    'METRICS AND LOGGING', [1460 110], 12};
for i = 1:size(labels,1)
    try
        a = Simulink.Annotation(mdl, labels{i,1});           % R2018a+ syntax
    catch
        a = Simulink.Annotation([mdl '/' labels{i,1}]);      % legacy path syntax
    end
    p = labels{i,2};
    try
        a.Position = p;                                      % [x y] in newer releases
    catch
        a.Position = [p(1) p(2) p(1)+420 p(2)+26];           % [l t r b] in older releases
    end
    a.FontSize = labels{i,3};
    a.FontWeight = 'bold';
end

%% Save and open:
%%

save_system(mdl);                                  % Save the completed model.
open_system(mdl);                                  % Open it in the editor.
fprintf('\nModel %s.slx created.\n', mdl);
fprintf('Run it with the green Run button, or:  sim(''%s'')\n', mdl);
fprintf('SNR point: edit the "SNR (dB)" Constant block. Equalizer/channel: edit config.m.\n');

