%% MATLAB Function block scripts (via the Stateflow API):
%%

rt = sfroot;                                       % Root object giving access to the block scripts.
names = keys(mfScript);                            % All MATLAB Function block names.
for i = 1:numel(names)
    ch = rt.find('-isa', 'Stateflow.EMChart', 'Path', [mdl '/' names{i}]);   % Script object of this block.
    ch.Script = mfScript(names{i});                % Thin fixed-size extrinsic script installed.
end

