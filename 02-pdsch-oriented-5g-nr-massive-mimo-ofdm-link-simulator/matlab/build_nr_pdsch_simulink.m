
%% Simulink Testbench Builder:
%%
%{
The build_nr_pdsch_simulink script constructs the Simulink testbench of the PDSCH-oriented Massive MIMO-OFDM
link-level simulator, mirroring the verified processing chain from transport block to CRC decision of the MATLAB main
script. It is run once from the folder that contains the simulator source files. The script writes seven thin wrapper
functions, the sl-prefixed files, that connect the Simulink blocks to the existing verified functions, keeping any
wrapper file that already exists, and then programmatically draws, wires, configures, saves, and opens the model
NR_PDSCH_LinkLevel_Sim.slx.

The model executes one Monte Carlo frame per simulation step at a fixed step of one. The transmitter area hosts the
PDSCH Transmitter block on build_frame. The channel area hosts the MIMO Channel Generator on generate_channel, the
Wideband SVD Precoder on compute_precoder, and the Channel plus AWGN block on apply_mimo_channel. The receiver area
hosts the LS Channel Estimator on estimate_effective_channel_ls and the ZF-MMSE Equalizer on equalize_mimo. The metric
area hosts the Demap Descramble CRC Metrics block on compute_frame_metrics, the per-frame displays, the workspace
logs, and the cumulative BER path.

The verification gates execute in the model InitFcn before the first step, exactly as in the main script, so no
result can be produced on an unverified chain. The SNR is a Constant block with a default of twenty decibels; the
equalizer, the channel model, and every other parameter follow config.m, the single source of the locked
configuration. Forty frames reproduce one SNR point of the executed simulation, and the StopFcn prints the aggregated
BER, BLER, EVM, NMSE, and capacity.

Auxiliary functions:

    config
        Locked simulation configuration providing every model dimension and parameter.

    build_frame
        Deterministic layout probe fixing the signal dimensions of the model.

Input:

    Simulator source files        The verified MATLAB chain in the current folder.

Output:

    sl_*.m wrapper files          Thin wrappers connecting the blocks to the verified chain, written if missing.
    NR_PDSCH_LinkLevel_Sim.slx    Complete wired and configured testbench model, saved and opened.
%}


%% Locked configuration and derived dimensions:
%%

clear; clc;
addpath(genpath(pwd));                             % Add the project directory and all subdirectories to the MATLAB path.
cfg = config();                                    % Load the locked simulation configuration.
s0 = rng; frame0 = build_frame(cfg); rng(s0);      % Deterministic layout probe with the random state restored.
nSym  = cfg.nSymbols;  nSC = cfg.nSC;              % Grid dimensions of the model signals.
nTx   = cfg.nTx;       nRx = cfg.nRx;   nL = cfg.nLayers;      % Antenna and layer dimensions.
nData = size(frame0.dataPositions,1);              % Data resource elements per slot.
nBitsCrc = length(frame0.bitsCrc);                 % Payload plus CRC24A bits per frame.
fprintf('Configuration: %dx%d, L = %d, %s, %d data REs, %d CRC-attached bits\n', ...
    nTx, nRx, nL, upper(cfg.modulation), nData, nBitsCrc);

%% Wrapper functions connecting Simulink blocks to the verified chain:
%%

wrappers = { ...
'sl_layout.m', sprintf([ ...
'function [dmrsMask, dmrsValues, dataPositions] = sl_layout()\n' ...
'%% Deterministic frame layout (DM-RS mask, Gold-seeded pilot values, data\n' ...
'%% positions) shared by the receiver blocks. The layout of build_frame is\n' ...
'%% independent of the random payload, so the RNG state is snapshotted and\n' ...
'%% restored around the probe call to keep the Monte Carlo stream intact.\n' ...
'cfg = config();\n' ...
's = rng; f = build_frame(cfg); rng(s);\n' ...
'dmrsMask = f.dmrsMask; dmrsValues = f.dmrsValues; dataPositions = f.dataPositions;\n' ...
'end\n']), ...
'sl_transmitter.m', sprintf([ ...
'function [layerGrid, bitsCrc] = sl_transmitter(~)\n' ...
'%% One PDSCH frame per step: transport block, CRC24A, Gold scrambling,\n' ...
'%% unit-power Gray QAM, layer mapping, and DM-RS insertion (build_frame).\n' ...
'cfg = config();\n' ...
'f = build_frame(cfg);\n' ...
'layerGrid = f.layerGrid;\n' ...
'bitsCrc = double(f.bitsCrc);\n' ...
'end\n']), ...
'sl_channel.m', sprintf([ ...
'function H = sl_channel(~)\n' ...
'%% One independent channel realization per step (generate_channel):\n' ...
'%% AWGN, flat/iid Rayleigh, Rician, or TDL per config.m.\n' ...
'cfg = config();\n' ...
'H = generate_channel(cfg);\n' ...
'end\n']), ...
'sl_precoder.m', sprintf([ ...
'function W = sl_precoder(H)\n' ...
'%% Wideband SVD eigenbeamforming precoder (compute_precoder).\n' ...
'cfg = config();\n' ...
'W = compute_precoder(cfg, H);\n' ...
'end\n']), ...
'sl_apply_channel.m', sprintf([ ...
'function [rxGrid, noiseVar] = sl_apply_channel(layerGrid, H, W, snrDb)\n' ...
'%% Effective channel and receiver noise, y = HWs + n, at the\n' ...
'%% fixed total-transmit-SNR convention (apply_mimo_channel).\n' ...
'[rxGrid, noiseVar] = apply_mimo_channel(layerGrid, H, W, snrDb);\n' ...
'end\n']), ...
'sl_estimator.m', sprintf([ ...
'function Ghat = sl_estimator(rxGrid)\n' ...
'%% LS estimation on the DM-RS: time averaging over the DM-RS\n' ...
'%% symbols and linear frequency interpolation (estimate_effective_channel_ls).\n' ...
'cfg = config();\n' ...
'[dmrsMask, dmrsValues, ~] = sl_layout();\n' ...
'f.dmrsMask = dmrsMask; f.dmrsValues = dmrsValues;\n' ...
'Ghat = estimate_effective_channel_ls(rxGrid, f, cfg);\n' ...
'end\n']), ...
'sl_equalizer.m', sprintf([ ...
'function shat = sl_equalizer(rxGrid, Ghat, noiseVar)\n' ...
'%% ZF or unbiased MMSE per config.m (equalize_mimo).\n' ...
'cfg = config();\n' ...
'[~, ~, dataPositions] = sl_layout();\n' ...
'shat = equalize_mimo(rxGrid, Ghat, dataPositions, noiseVar, cfg);\n' ...
'end\n']), ...
'sl_metrics.m', sprintf([ ...
'function [bitErrors, nBits, blockError, evm, nmse, capacity] = sl_metrics(shat, layerGrid, bitsCrc, Ghat, H, W, snrDb)\n' ...
'%% Demapping, descrambling, CRC decision, and the simulator metric set.\n' ...
'cfg = config();\n' ...
'[~, ~, dataPositions] = sl_layout();\n' ...
'f.bitsCrc = uint8(bitsCrc(:));\n' ...
'f.payload = uint8(bitsCrc(1:end-cfg.crcLen));\n' ...
'f.layerGrid = layerGrid;\n' ...
'f.dataPositions = dataPositions;\n' ...
'met = compute_frame_metrics(shat, f, Ghat, H, W, snrDb, cfg);\n' ...
'bitErrors = met.bitErrors; nBits = met.nBits; blockError = met.blockError;\n' ...
'evm = met.evm; nmse = met.nmse; capacity = met.capacityBpsHz;\n' ...
'end\n'])};
for i = 1:2:numel(wrappers)
    if exist(wrappers{i}, 'file')
        fprintf('Kept existing %s\n', wrappers{i});   % Formatted wrapper files are preserved.
        continue
    end
    fid = fopen(wrappers{i}, 'w'); fprintf(fid, '%s', wrappers{i+1}); fclose(fid);
    fprintf('Wrote %s\n', wrappers{i});
end

%% Model shell:
%%

mdl = 'NR_PDSCH_LinkLevel_Sim';                    % Testbench model name.
if bdIsLoaded(mdl); close_system(mdl, 0); end      % Close a loaded copy without saving.
if exist([mdl '.slx'], 'file'); delete([mdl '.slx']); end     % Rebuild from a clean file.
new_system(mdl); open_system(mdl);                 % Fresh model shell in the editor.
set_param(mdl, 'SolverType', 'Fixed-step', 'Solver', 'FixedStepDiscrete', ...     % One frame per fixed step;
    'FixedStep', '1', 'StopTime', num2str(cfg.numFrames - 1), ...                 % forty steps reproduce one
    'ReturnWorkspaceOutputs', 'off');                                              % SNR point of the sweep.
set_param(mdl, 'InitFcn', sprintf([ ...            % Gates run before the first step, as in the main script.
    'addpath(genpath(pwd));\ncfg = config();\nrng(cfg.randomSeed);\n' ...
    'run_verification_gates(cfg);   %% gates before results']));
set_param(mdl, 'StopFcn', sprintf([ ...            % Aggregate printout after the final step.
    'ber = sum(log_bitErrors)/sum(log_nBits);\n' ...
    'bler = mean(log_blockError);\n' ...
    'fprintf(''\\n=== Simulink run aggregate (one SNR point) ===\\n'');\n' ...
    'fprintf(''BER  = %%.5g\\nBLER = %%.4g\\nEVM  = %%.4g %%%%\\nNMSE = %%.5g\\nCapacity = %%.4g bit/s/Hz\\n'', ...\n' ...
    '    ber, bler, 100*mean(log_evm), mean(log_nmse), mean(log_capacity));']));

%% Helper for MATLAB Function block scripts (thin, fixed-size, extrinsic):
%%

mfScript = containers.Map();                       % Block scripts keyed by block name.
mfScript('PDSCH Transmitter') = sprintf([ ...
'function [layerGrid, bitsCrc] = fcn(tick) %%#ok<INUSD>\n' ...
'%% build_frame: TB -> CRC24A -> Gold scrambling -> QAM -> layers + DM-RS\n' ...
'coder.extrinsic(''sl_transmitter'');\n' ...
'layerGrid = complex(zeros(%d,%d,%d));\n' ...
'bitsCrc = zeros(%d,1);\n' ...
'[layerGrid, bitsCrc] = sl_transmitter(tick);\n'], nSym, nSC, nL, nBitsCrc);
mfScript('MIMO Channel Generator') = sprintf([ ...
'function H = fcn(tick) %%#ok<INUSD>\n' ...
'%% generate_channel: one realization per frame (AWGN/Rayleigh/Rician/TDL)\n' ...
'coder.extrinsic(''sl_channel'');\n' ...
'H = complex(zeros(%d,%d,%d));\n' ...
'H = sl_channel(tick);\n'], nSC, nRx, nTx);
mfScript('Wideband SVD Precoder') = sprintf([ ...
'function W = fcn(H)\n' ...
'%% compute_precoder: wideband eigenbeamforming\n' ...
'coder.extrinsic(''sl_precoder'');\n' ...
'W = complex(zeros(%d,%d));\n' ...
'W = sl_precoder(H);\n'], nTx, nL);
mfScript('MIMO Channel + AWGN') = sprintf([ ...
'function [rxGrid, noiseVar] = fcn(layerGrid, H, W, snrDb)\n' ...
'%% apply_mimo_channel: y = HWs + n, total-transmit-SNR noise\n' ...
'coder.extrinsic(''sl_apply_channel'');\n' ...
'rxGrid = complex(zeros(%d,%d,%d));\n' ...
'noiseVar = 0;\n' ...
'[rxGrid, noiseVar] = sl_apply_channel(layerGrid, H, W, snrDb);\n'], nSym, nSC, nRx);
mfScript('LS Channel Estimator') = sprintf([ ...
'function Ghat = fcn(rxGrid)\n' ...
'%% estimate_effective_channel_ls: LS on DM-RS\n' ...
'coder.extrinsic(''sl_estimator'');\n' ...
'Ghat = complex(zeros(%d,%d,%d));\n' ...
'Ghat = sl_estimator(rxGrid);\n'], nSC, nRx, nL);
mfScript('ZF-MMSE Equalizer') = sprintf([ ...
'function shat = fcn(rxGrid, Ghat, noiseVar)\n' ...
'%% equalize_mimo: ZF or unbiased MMSE, per config.m\n' ...
'coder.extrinsic(''sl_equalizer'');\n' ...
'shat = complex(zeros(%d,%d));\n' ...
'shat = sl_equalizer(rxGrid, Ghat, noiseVar);\n'], nData, nL);
mfScript('Demap Descramble CRC Metrics') = sprintf([ ...
'function [bitErrors, nBits, blockError, evm, nmse, capacity] = fcn(shat, layerGrid, bitsCrc, Ghat, H, W, snrDb)\n' ...
'%% compute_frame_metrics: BER, BLER, EVM, NMSE, and layer capacity\n' ...
'coder.extrinsic(''sl_metrics'');\n' ...
'bitErrors = 0; nBits = 0; blockError = 0; evm = 0; nmse = 0; capacity = 0;\n' ...
'[bitErrors, nBits, blockError, evm, nmse, capacity] = sl_metrics(shat, layerGrid, bitsCrc, Ghat, H, W, snrDb);\n']);

